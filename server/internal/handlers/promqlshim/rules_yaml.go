package promqlshim

import (
	"fmt"
	"log/slog"
	"net/http"
	"strings"
)

// handleRulesYAML renders the vmalert rule file from BuiltinRules(). vmalert
// is started with `-rule=http://fleet-api:4000/internal/vmalert/rules.yml`,
// so it pulls this every reload. The YAML is a thin stub — each rule's
// expression is the canonical fleet_alert{rule_id="…"} selector. The
// actual rule logic (SQL, for, severity) is the Go data in rules.go and
// the SQL constants in queries.go.
func (h *Handler) handleRulesYAML(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodGet {
		w.WriteHeader(http.StatusMethodNotAllowed)
		return
	}
	w.Header().Set("Content-Type", "application/yaml")
	if _, err := w.Write([]byte(renderRulesYAML(BuiltinRules()))); err != nil {
		slog.Error("promqlshim: failed to write rules.yml", "error", err)
	}
}

// renderRulesYAML produces the vmalert rule file content. Exported via
// handleRulesYAML; the function is also unit-testable directly.
func renderRulesYAML(rules []Rule) string {
	var b strings.Builder
	b.WriteString("# Generated by fleet-api's promqlshim package — DO NOT EDIT.\n")
	b.WriteString("# Source of truth lives in server/internal/handlers/promqlshim/rules.go;\n")
	b.WriteString("# the SQL each rule evaluates lives in queries.go.\n")
	b.WriteString("groups:\n")
	b.WriteString("  - name: proto-fleet-builtins\n")
	b.WriteString("    interval: 30s\n")
	b.WriteString("    rules:\n")
	for _, r := range rules {
		fmt.Fprintf(&b, "      - alert: %s\n", r.ID)
		fmt.Fprintf(&b, "        expr: fleet_alert{rule_id=\"%s\"}\n", r.ID)
		fmt.Fprintf(&b, "        for: %s\n", r.For)
		fmt.Fprintf(&b, "        labels:\n")
		fmt.Fprintf(&b, "          severity: %s\n", r.Severity)
		fmt.Fprintf(&b, "          rule_id: %s\n", r.ID)
		fmt.Fprintf(&b, "        annotations:\n")
		fmt.Fprintf(&b, "          summary: %q\n", r.Description)
	}
	return b.String()
}
